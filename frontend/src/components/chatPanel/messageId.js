// Стабильный локальный идентификатор пузыря сообщения — для React key в MessageList.
// Бэкенд не отдаёт per-message id, а индекс массива в качестве key ломается при
// догрузке старых сообщений вверх (все индексы сдвигаются, React переиспользует
// DOM не по назначению — путается состояние внутри Message: тултипы, режим
// «исходник», открытые детали). Поэтому каждый пузырь получает mid один раз при
// создании и сохраняет его при иммутабельных обновлениях (через spread).
//
// Уникальность нужна лишь в пределах вкладки/сессии, поэтому хватает счётчика.

let counter = 0;

/** Выдать новый стабильный id пузыря. */
export const nextMessageId = () => `m${(counter += 1).toString(36)}`;
